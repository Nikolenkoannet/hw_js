'use strict'

let a = prompt('number');
let b = prompt('number');

a = Number(a);
b = Number(b);

const additionResult = a + b;
console.log(`${a} + ${b} = ${additionResult}`);

const subtractionResult = a - b;
console.log(`${a} - ${b} = ${subtractionResult}`);

const multiplicationResult = a * b;
console.log(`${a} * ${b} = ${multiplicationResult}`);

const divisionResult = a / b;
console.log(`${a} / ${b} = ${divisionResult}`);
